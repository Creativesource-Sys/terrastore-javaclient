package terrastore.client.test.pojostest;

/**
 * This class is exactly like the Address class, except I've added yet another field: zip
 *
 * @author mats@henricson.se
 */
public class Address2 {
    private String street;
    private int zip;

    protected Address2() {
        // Empty
    }

    public Address2(String street, int zip) {
        this.street = street;
        this.zip = zip;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public int getZip() {
        return zip;
    }

    public void setZip(int zip) {
        this.zip = zip;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Address2 address2 = (Address2) o;

        if (zip != address2.zip) return false;
        if (street != null ? !street.equals(address2.street) : address2.street != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = street != null ? street.hashCode() : 0;
        result = 31 * result + zip;
        return result;
    }
}
